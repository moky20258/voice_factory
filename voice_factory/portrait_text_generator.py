def get_texts_for_portrait(portrait: str, count: int = 30) -> List[str]:
    """
    根据人物画像获取训练文本
    
    Args:
        portrait: 人物画像描述
        count: 需要的文本数量
    
    Returns:
        list: 文本列表
    """
    # 精确匹配
    if portrait in PORTRAIT_TEXTS:
        texts = PORTRAIT_TEXTS[portrait]
        if len(texts) >= count:
            return random.sample(texts, count)
        else:
            # 如果文本不足，重复使用
            result = texts.copy()
            while len(result) < count:
                result.extend(random.sample(texts, min(count - len(result), len(texts))))
            return result
    
    # 模糊匹配
    matched_texts = fuzzy_match_texts(portrait)
    if matched_texts:
        if len(matched_texts) >= count:
            return random.sample(matched_texts, count)
        else:
            result = matched_texts.copy()
            while len(result) < count:
                result.extend(random.sample(matched_texts, min(count - len(result), len(matched_texts))))
            return result
    
    # 默认使用中年男中音的文本
    print(f"⚠️  未找到匹配的文本: {portrait}")
    print("💡 使用默认文本（中年男中音）")
    default_texts = PORTRAIT_TEXTS["中年男中音"]
    return random.sample(default_texts, min(count, len(default_texts)))


def fuzzy_match_texts(portrait: str) -> Optional[List[str]]:
    """
    模糊匹配文本
    
    Args:
        portrait: 人物画像描述
    
    Returns:
        list or None: 匹配的文本列表
    """
    portrait_lower = portrait.lower()
    
    has_male = any(k in portrait_lower for k in ['男', 'male'])
    has_female = any(k in portrait_lower for k in ['女', 'female'])
    has_young = any(k in portrait_lower for k in ['青年', '年轻', 'young'])
    has_middle = any(k in portrait_lower for k in ['中年', 'middle'])
    has_high = any(k in portrait_lower for k in ['高音', 'high'])
    has_middle_tone = any(k in portrait_lower for k in ['中音', 'middle'])
    has_low = any(k in portrait_lower for k in ['低音', 'low'])
    
    # 组合匹配
    if has_male:
        if has_middle and has_middle_tone:
            return PORTRAIT_TEXTS.get("中年男中音")
        elif has_middle and has_low:
            return PORTRAIT_TEXTS.get("中年男低音")
        elif (has_young or not has_middle) and has_high:
            return PORTRAIT_TEXTS.get("青年男高音")
        elif has_middle:
            return PORTRAIT_TEXTS.get("中年男中音")
    elif has_female:
        if (has_young or not has_middle) and has_high:
            return PORTRAIT_TEXTS.get("青年女高音")
        elif (has_young or not has_middle) and has_middle_tone:
            return PORTRAIT_TEXTS.get("青年女中音")
        elif has_middle and has_low:
            return PORTRAIT_TEXTS.get("中年女低音")
        elif has_young:
            return PORTRAIT_TEXTS.get("青年女中音")
    
    return None


def save_texts_to_file(portrait: str, texts: List[str], output_dir: str = "texts"):
    """
    保存文本到文件
    
    Args:
        portrait: 人物画像描述
        texts: 文本列表
        output_dir: 输出目录
    """
    os.makedirs(output_dir, exist_ok=True)
    
    # 文件名使用画像名称
    safe_name = portrait.replace(" ", "_").replace("/", "_")
    filename = f"portrait_{safe_name}.txt"
    filepath = os.path.join(output_dir, filename)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        for text in texts:
            f.write(text + '\n')
    
    print(f"✅ 文本已保存: {filepath}")
    print(f"   文本数量: {len(texts)}")
    return filepath


